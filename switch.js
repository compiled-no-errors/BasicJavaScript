var a = 6.5;  //can we write floating point number in the place of 9? (like 9.5) answer is yes (but this is only in java script)


switch (a) {
    case 3: {
        console.log(a);
    }
        
        break;

    case 6.5: {
        console.log(a*2);
    }
        
       break;

    case 9:{
        console.log(a*3);
    }
        
      break;

    default:
        break;
}


// switch case is an object
// switch and ifelse are conditional statements   
// for while do while are iteration statements 
